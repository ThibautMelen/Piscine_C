#include "lib_doop.h"

int		ft_plus(int v1, int v2)
{
	return (v1 + v2);
}

int		ft_moins(int v1, int v2)
{
	return (v1 - v2);
}

int		ft_mul(int v1, int v2)
{
	return (v1 * v2);
}

int		ft_div(int v1, int v2)
{
	return (v1 / v2);
}

int		ft_mod(int v1, int v2)
{
	return (v1 % v2);
}
